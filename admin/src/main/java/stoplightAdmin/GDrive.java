/**
 * Code that establishes a connection with google via a service account
 */
public class GDrive {
    /** Application name. */
    private static final String APPLICATION_NAME =
            "Drive API Java Quickstart";

    /** Directory to store user credentials for this application. */
    private static final java.io.File DATA_STORE_DIR = new java.io.File(
            System.getProperty("user.home"), ".credentials/drive-java-quickstart");

    /** Global instance of the {@link FileDataStoreFactory}. */
    private static FileDataStoreFactory DATA_STORE_FACTORY;

    /** Global instance of the JSON factory. */
    private static final JsonFactory JSON_FACTORY =
            JacksonFactory.getDefaultInstance();

    /** Global instance of the HTTP transport. */
    private static HttpTransport HTTP_TRANSPORT;

    /** Global instance of the scopes required by this quickstart.
     *
     * If modifying these scopes, delete your previously saved credentials
     * at ~/.credentials/drive-java-quickstart
     */
    private static final List<String> SCOPES =
            Arrays.asList(DriveScopes.DRIVE);

    static {
        try {
            HTTP_TRANSPORT = GoogleNetHttpTransport.newTrustedTransport();
            DATA_STORE_FACTORY = new FileDataStoreFactory(DATA_STORE_DIR);
        } catch (Throwable t) {
            t.printStackTrace();
            System.exit(1);
        }
    }

    /**
     * Creates an authorized Credential object.
     * @return an authorized Credential object.
     * @throws IOException
     */
    public static Credential authorize2() throws IOException{
        String client_x509_cert_url = System.getenv("client_x509_cert_url");
        String auth_provider_x509_cert_url = System.getenv("auth_provider_x509_cert_url");
        String token_uri = System.getenv("token_uri");
        String auth_uri = System.getenv("auth_uri");
        String client_id = System.getenv("client_id");
        String client_email = System.getenv("client_email");
        String private_key = System.getenv("private_key");
        String private_key_id = System.getenv("private_key_id");
        String project_id = System.getenv("project_id");
        String type = System.getenv("project_id");
        System.out.println(client_x509_cert_url);
        System.out.println("Pre Input Stream");
        InputStream in = GDrive.class.getClassLoader().getResourceAsStream("TheBuzz.json");
        System.out.println("Post INput Stream");
        return GoogleCredential.fromStream(in, HTTP_TRANSPORT,JSON_FACTORY)
                .createScoped(Collections.singleton(DriveScopes.DRIVE));
    }


    /**
     * Build and return an authorized Drive client service.
     * @return an authorized Drive client service
     * @throws IOException
     */
    public static Drive getDriveService() throws IOException {
        Credential credential = authorize2();
        return new Drive.Builder(
                HTTP_TRANSPORT, JSON_FACTORY, credential)
                .setApplicationName(APPLICATION_NAME)
                .build();
    }

    public static void main(String[] args) throws IOException {
        // Build a new authorized API client service.
        Drive service = getDriveService();

        // Print the names and IDs for up to 10 files.
        FileList result = service.files().list()
                .setMaxResults(10)
                .setFields("nextPageToken, files(id, name)")
                .execute();
        List<File> files = result.getItems();
        if (files == null || files.size() == 0) {
            System.out.println("No files found.");
        } else {
            System.out.println("Files:");
            for (File file : files) {
                System.out.printf("%s (%s)\n", file.getTitle(), file.getId());
            }
        }
    }

}